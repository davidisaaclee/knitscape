import { invert, cloneDeep } from "lodash";
import { flipHoriz } from "./utils";

export type Stitch = number;
export type Pattern = {
  rows: Stitch[][];
};

// maps stitch material id -> CSS color string
export type Palette = Record<number, string>;

// eslint-disable-next-line @typescript-eslint/no-redeclare
export const Pattern = {
  create(rows: Stitch[][] = []): Pattern {
    return { rows };
  },

  extents(p: Pattern) {
    // assumes rectangular pattern
    return {
      width: p.rows[0] == null ? 0 : p.rows[0].length,
      height: p.rows.length,
    };
  },

  stitchCount(p: Pattern) {
    const { width, height } = Pattern.extents(p);
    return height * width;
  },

  flippingVertically(p: Pattern) {
    const out = cloneDeep(p);
    out.rows.reverse();
    return out;
  },

  stitchAt(p: Pattern, c: Readonly<{ row: number; column: number }>): number {
    return p.rows[c.row]?.[c.column] ?? -1;
  },

  countUntilStitchChange(
    p: Pattern,
    start: Cursor
  ): {
    count: number;
    stitch?: { row: number; column: number; colorIndex: number };
  } {
    const patternExtents = Pattern.extents(p);

    const offset = Cursor.stitchCountSinceStartOfPattern(start, patternExtents);
    const startStitch = Pattern.stitchAt(p, start);

    for (let i = 0; i + offset < Pattern.stitchCount(p); i++) {
      const cursorIndex = Cursor.offsetBy(start, i, patternExtents);
      const colorIndex = Pattern.stitchAt(p, cursorIndex);
      if (colorIndex !== startStitch) {
        return {
          count: i,
          stitch: {
            row: cursorIndex.row,
            column: cursorIndex.column,
            colorIndex,
          },
        };
      }
    }

    return { count: Pattern.stitchCount(p) - offset };
  },
};

export interface Cursor {
  row: number;
  column: number;
  directionHorizontal: "ltr" | "rtl";
  directionVertical: "ascending" | "descending";
}

// eslint-disable-next-line @typescript-eslint/no-redeclare
export const Cursor = {
  create({
    row = 0,
    column = 0,
    directionHorizontal = "ltr",
    directionVertical = "ascending",
  }: Partial<{
    row: number;
    column: number;
    directionHorizontal: "ltr" | "rtl";
    directionVertical: "ascending" | "descending";
  }> = {}) {
    return {
      row,
      column,
      directionHorizontal,
      directionVertical,
    };
  },

  offsetBy(
    prev: Cursor,
    delta: number,
    patternExtents: { width: number; height: number }
  ): Cursor {
    const prevPosition =
      (prev.directionHorizontal === "ltr"
        ? prev.column
        : patternExtents.width - 1 - prev.column) +
      prev.row * patternExtents.width;
    const nextPosition = Math.max(
      Math.min(
        prevPosition + delta,
        patternExtents.width * patternExtents.height - 1
      ),
      0
    );
    const outRow = Math.floor(nextPosition / patternExtents.width);
    const outDirH = flipHoriz(
      prev.directionHorizontal,
      (outRow - prev.row) % 2 !== 0
    );
    // before applying horizontal direction
    const columnOffset = nextPosition % patternExtents.width;
    return {
      ...prev,
      column:
        outDirH === "ltr"
          ? columnOffset
          : patternExtents.width - 1 - columnOffset,
      row: outRow,
      directionHorizontal: outDirH,
    };
  },

  stitchCountSinceStartOfPattern(
    c: Cursor,
    patternExtents: { width: number; height: number }
  ): number {
    const countInCurrentRow =
      c.directionHorizontal === "ltr"
        ? c.column
        : patternExtents.width - 1 - c.column;
    const countForPreviousRows = c.row * patternExtents.width;
    return countInCurrentRow + countForPreviousRows;
  },

  stitchCountToPosition(
    start: Cursor,
    [targetColumn, targetRow]: [column: number, row: number],
    patternExtents: { width: number; height: number }
  ): number {
    return (
      Cursor.stitchCountSinceStartOfPattern(
        {
          ...start,
          row: targetRow,
          column: targetColumn,
          directionHorizontal: flipHoriz(
            start.directionHorizontal,
            (targetRow - start.row) % 2 !== 0
          ),
        },
        patternExtents
      ) - Cursor.stitchCountSinceStartOfPattern(start, patternExtents)
    );
  },
};

function stitch(mat: number): Stitch {
  return mat;
}

export function patternFromImageData(imageData: ImageData): [Pattern, Palette] {
  // color -> index
  const reversePalette: Record<string, number> = {};
  const pattern = Pattern.create();
  const pixelWidth = 4;

  let rowBuffer: Stitch[] = [];

  for (let i = 0; i <= imageData.data.length - pixelWidth; i += pixelWidth) {
    const color = [
      imageData.data[i + 0],
      imageData.data[i + 1],
      imageData.data[i + 2],
      // omit alpha
    ];
    const colorHex = `#${color
      .map((c) => c.toString(16).padStart(2, "0"))
      .join("")}`;
    const colorIndex = (() => {
      if (reversePalette[colorHex] == null) {
        reversePalette[colorHex] = Object.keys(reversePalette).length;
      }
      return reversePalette[colorHex]!;
    })();

    rowBuffer.push(colorIndex);
    if ((i / pixelWidth) % imageData.width === imageData.width - 1) {
      pattern.rows.push(rowBuffer);
      rowBuffer = [];
    }
  }

  return [pattern, invert(reversePalette)];
}

