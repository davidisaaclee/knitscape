import { invert } from "lodash";

export type Stitch = number;
export type Pattern = {
  rows: Stitch[][];
};

// maps stitch material id -> CSS color string
export type Palette = Record<number, string>;

// eslint-disable-next-line @typescript-eslint/no-redeclare
export const Pattern = {
  create(rows: Stitch[][] = []): Pattern {
    return { rows };
  },

  extents(p: Pattern) {
    // assumes rectangular pattern
    return {
      width: p.rows[0] == null ? 0 : p.rows[0].length,
      height: p.rows.length,
    };
  },
};

export interface Cursor {
  row: number;
  column: number;
  directionHorizontal: "ltr" | "rtl";
  directionVertical: "ascending" | "descending";
}

// eslint-disable-next-line @typescript-eslint/no-redeclare
export const Cursor = {
  create({
    row = 0,
    column = 0,
    directionHorizontal = "ltr",
    directionVertical = "ascending",
  }: Partial<{
    row: number;
    column: number;
    directionHorizontal: "ltr" | "rtl";
    directionVertical: "ascending" | "descending";
  }> = {}) {
    return {
      row,
      column,
      directionHorizontal,
      directionVertical,
    };
  },
};

function stitch(mat: number): Stitch {
  return mat;
}

export function patternFromImageData(imageData: ImageData): [Pattern, Palette] {
  // color -> index
  const reversePalette: Record<string, number> = {};
  const pattern = Pattern.create();
  const pixelWidth = 4;

  let rowBuffer: Stitch[] = [];

  for (let i = 0; i <= imageData.data.length - pixelWidth; i += pixelWidth) {
    const color = [
      imageData.data[i + 0],
      imageData.data[i + 1],
      imageData.data[i + 2],
      // omit alpha
    ];
    const colorHex = `#${color
      .map((c) => c.toString(16).padStart(2, "0"))
      .join("")}`;
    const colorIndex = (() => {
      if (reversePalette[colorHex] == null) {
        reversePalette[colorHex] = Object.keys(reversePalette).length;
      }
      return reversePalette[colorHex]!;
    })();

    rowBuffer.push(colorIndex);
    if ((i / pixelWidth) % imageData.width === imageData.width - 1) {
      pattern.rows.push(rowBuffer);
      rowBuffer = [];
    }
  }

  return [pattern, invert(reversePalette)];
}

